<?php
//This function shows that how to return variable from function

    //Define function
    function name()
    {
        //initialize variable
        $nm="My name is Vaishnavi";
        
        //return variable (return keyword is used to return variable)
        //when we use return keyword in function it means the function is trminated there
        return $nm;
    }

    //call function and print the value stored in variable 
    echo name();
?>