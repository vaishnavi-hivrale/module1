<?php
// This program shows the use of increment operator

    $a=10;

    //print value of a
    echo "Value of a is: $a <br>";

    //pre increment
    echo "<h4> Pre Increment (++a) </h4>";
    echo "value of a before pre incremrnt: $a <br>"; 

    ++$a;

    echo "Value of a after pre increment: $a <br>";


    //post icrement
    echo "<h4> Post Inctrement (a++) </h4>";
    echo "Value of a before post increment: $a <br>";
    
    $a++;

    echo "value of a after post increment: $a";

?>