<?php
// This program shows the use of decrement operator

    $a=22;

    //print value of a
    echo "Value of a: $a <br>";

    //pre decrement
    echo "<h4> Pre Decrement (--a) </h4>";
    echo "Value of a before pre decrement: $a <br>";

    --$a;

    echo "Value of a after pre decrement: $a <br>";

    
    //post decrement
    echo "<h4> Post Decrement (a--) </h4>";
    echo "Value of a before post decrement: $a <br>";

    $a--;

    echo "Value of a after post decrement: $a";
?>